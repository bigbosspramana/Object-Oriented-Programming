public class EnchanceFor {
    public static void main (String []args) {
        // Membuat sebuah array smbungKata
        String[] smbungKata = {"Saya", "adalah", "orang", "paling", "Ganteng"};
        // Membuat sebuah for loop
        for (String kata : smbungKata){
            // Menampilkan isi dari kata
            System.out.println(kata);
        }
    }
}

// Sistem ini, menampilkan setiap isi yang ada di dalam array dengan menggunakan for loop atau for each loop.
