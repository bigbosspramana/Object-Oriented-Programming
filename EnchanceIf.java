public class EnchanceIf {
    public static void main (String []args){
        // Menginisialisasikan makanan
        String makanan = "Nasi Goreng";
        // Melakukan pengkondisian untuk menentukan apakah nilai dari makanan adalah nasi goreng atau bukan
        String pesanan = (makanan == "Nasi Goreng") ? "Ini Nasi Goreng" : "Ini Bukan Nasi Goreng";
        // Menampilkan hasil dari pengkondisian
        System.out.println(pesanan);
    }
}

// Dalam sistem ini, menjalankan pengkondisian yang dimana dalam kode ini akan menentukan apakah value dari makanan adalah nasi goreng
// atau bukan, dengan menggunakan enchance if sebuah kode dapat menjadi lebih ringkas dan meningkatkan keterbaccaan kode.